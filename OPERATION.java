//Program with all Operations.
class OPERATION
{
    public static void main()
    {
        int a = 10, b = 20;
        int sum = a + b;
        int sub = a - b;
        int pro = a * b;
        int quo= a * b;
        int rem = a % b;
        System.out.println("The result of Addition = " + sum);
        System.out.println("The result of Subtraction = " + sub);
        System.out.println("The result of Multiplication = " + pro);
        System.out.println("The Quotient = " + quo);
        System.out.println("The Remainder = " + rem);
    }
}